public class SimpleTypes {

    public void demo(){
        boolean aVar = false;
        System.out.println("Boolean " + aVar);
        char bVar = '1';
        System.out.println("char " + bVar);
        byte cVar = 100;
        System.out.println("byte " + cVar);
        short dVar = 32767;
        System.out.println("short " + dVar);
        int eVar = -2147483648;
        System.out.println("int " + eVar);
        long fVar = 666;
        System.out.println("long " + fVar);
        float gVar = 89.64f;
        System.out.println("float " + gVar);
        double hVar = 11111111;
        System.out.println("double " + hVar);
          
    }
    
}
